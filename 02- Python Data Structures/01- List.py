
# * List Data Structue in Python: []

# list of integers
numbers = [1, 2, 3, 4, 5]

# string list
language = ['Python', 'Go', 'Rust']

# Mixed data tyles list
mixed_list = [0, 4.2, True, 'Hello']

# Nested list
matrix_list = [[1, 2], [1, 1], [0, 9]]

# combined two list
