
# Ternery Operatros in Python:

# Syntax:
# true_value if condition else false_value

# simple if else
age = 20
if age >= 18:
    print('Enjoy your adulthood ..')
else:
    print('Focus on your study ...')


# with ternery operators:
message = "Enjoy your Adulthood..." if age >= 18 else "Focus on your study..."
print(message)  # Enjoy your Adulthood ...
